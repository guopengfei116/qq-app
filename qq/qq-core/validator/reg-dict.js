const regDict = {
  // 数字
  number: /^\d+$/,
  // 域名
  domain: /^[\w-]+(\.[\w-]+)+$/,
  // 域名前缀
  domainPrefix: /^([\w-]+)(?=\.)/,
  // 域名后缀
  domainSuffix: /(\.[\w-]+)$/,
  // 网络地址：^x.x.x.x(x:0~255)$
  ip: /^(2([0-4]\d|5[0-5])|[0-1]?\d{1,2})(\.(2([0-4]\d|5[0-5])|[0-1]?\d{1,2})){3}$/,
  // 路径：/ 或 /*
  path: /^\/$|^\/[\w?#&%=-]+/,
  // 端口：^(0~65535)$
  port: /^0$|^([1-5]\d{0,4})$|^([6-9]\d{0,3})$|^(6[0-5][0-5][0-3][0-5])$/
};

export default regDict;
